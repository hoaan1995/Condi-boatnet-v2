compile_bot x86_64 debug.dbg "-static -DDEMONS_KILLER -DSELFREP -DDEBUG"
echo "Building - debug"
compile_bot x86_64 attack.dbg "-static -DDEBUG -DDEMONS_KILLER"
echo "Building - x86_64"
compile_bot x86_64 home.x86_64 "-static -DDEMONS_KILLER -DSELFREP"
